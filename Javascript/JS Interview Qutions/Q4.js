// Q4. Write a function that calculates the area of a rectangle.

// Answer:
function calculateAreaOfRectangle(length, width) {
    return length * width;
}

console.log(calculateAreaOfRectangle(10, 20)); 

//output: 200

// Arrow Function

let calculateAreaOfRectangle = (length, width) => length * width;

console.log(calculateAreaOfRectangle(10, 20)); 

//output: 200