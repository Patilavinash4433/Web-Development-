// Q2. Write a JavaScript program to print the number from 1 to 5.

// Answer:
for (let i=1;i<=5;i++){
        console.log(i);
}
//output: 1 2 3 4 5

let j=1;
while(j<=5){
    console.log("While loop: "+j);
    j++;
}
//output: 1 2 3 4 5

let k=1;
do {
    console.log("Do while loop: "+k);
    k++;
} while(k<=5);

//output: 1 2 3 4 5